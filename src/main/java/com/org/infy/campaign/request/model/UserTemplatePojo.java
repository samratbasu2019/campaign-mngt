package com.org.infy.campaign.request.model;

public class UserTemplatePojo {
	private Integer userid;
	private Integer userTemplateId;
	private String templateData;
	private String date;
	private String contactAlias;
	private String contactEmails;
	public Integer getUserid() {
		return userid;
	}
	public void setUserid(Integer userid) {
		this.userid = userid;
	}
	public Integer getUserTemplateId() {
		return userTemplateId;
	}
	public void setUserTemplateId(Integer userTemplateId) {
		this.userTemplateId = userTemplateId;
	}
	public String getTemplateData() {
		return templateData;
	}
	public void setTemplateData(String templateData) {
		this.templateData = templateData;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getContactAlias() {
		return contactAlias;
	}
	public void setContactAlias(String contactAlias) {
		this.contactAlias = contactAlias;
	}
	public String getContactEmails() {
		return contactEmails;
	}
	public void setContactEmails(String contactEmails) {
		this.contactEmails = contactEmails;
	}
	
	
	

}

